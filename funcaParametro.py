# Funções são blocos de código que realizam uma tarefa específica.
# Elas podem receber dados como entrada, que são chamados de parâmetros ou argumentos.

# Vamos definir uma função chamada 'saudacao' que aceita um parâmetro chamado 'nome'.
def saudacao(nome):
  """
  Esta função recebe um nome como parâmetro e imprime uma mensagem de saudação.
  """
  print(f"Olá, {nome}! Seja bem-vindo(a).")

# Agora, vamos chamar a função 'saudacao' e passar um valor para o parâmetro 'nome'.
# A string "Maria" é o argumento que estamos passando para a função.
print("Chamando a função com o nome 'Maria':")
saudacao("Maria")

print("\nChamando a função com o nome 'Carlos':")
saudacao("Carlos")

# Você também pode usar uma variável para passar como argumento.
aluno = "Ana"
print(f"\nChamando a função com a variável 'aluno' (valor: {aluno}):")
saudacao(aluno)

# Parâmetros permitem que funções sejam reutilizáveis com diferentes entradas.
print("\nChamando a função com diferentes nomes:")
nomes = ["João", "Luiza", "Pedro"]
for nome in nomes:
  saudacao(nome)  

# Isso demonstra como os parâmetros tornam as funções flexíveis e reutilizáveis.
